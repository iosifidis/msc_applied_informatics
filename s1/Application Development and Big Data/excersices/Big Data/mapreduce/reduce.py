#!/usr/bin/env python3
# Εισαγωγή της βιβλιοθήκης sys για πρόσβαση σε stdin και stdout
import sys

# Αρχικοποίηση μεταβλητών για την ανίχνευση αλλαγών κλειδιού
username = None  # Το τρέχον όνομα χρήστη
prev_username = None  # Το προηγούμενο όνομα χρήστη
occurrences = 0  # Πλήθος εμφανίσεων του κλειδιού

# Ανάγνωση κάθε γραμμής εισόδου από το standard input
for line in sys.stdin:
    # Αφαίρεση κενών χαρακτήρων και αλλαγών γραμμής από το τέλος της γραμμής
    line = line.strip()
    
    # Διαχωρισμός της γραμμής σε κλειδί και τιμή με βάση τον στηλοθέτη
    username, one = line.split("\t")
    one = int(one)  # Μετατροπή της τιμής σε ακέραιο

    # Ελέγχουμε αν το κλειδί (username) έχει αλλάξει
    if username != prev_username:
        if prev_username is not None:
            # Εκπομπή του προηγούμενου κλειδιού και του πλήθους εμφανίσεών του
            print(f"{prev_username}\t{occurrences}")
        # Επανεκκίνηση του μετρητή για το νέο κλειδί
        occurrences = one
    else:
        # Άθροιση των εμφανίσεων για το ίδιο κλειδί
        occurrences += one

    # Ενημέρωση του προηγούμενου κλειδιού
    prev_username = username

# Εκπομπή του τελευταίου κλειδιού και του πλήθους εμφανίσεών του
if prev_username is not None:
    print(f"{prev_username}\t{occurrences}")

