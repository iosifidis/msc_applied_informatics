#!/usr/bin/env python3

# Εισαγωγή της βιβλιοθήκης sys για πρόσβαση σε stdin και stdout
import sys

# Ανάγνωση κάθε γραμμής εισόδου από το standard input
for line in sys.stdin:
    # Αφαίρεση κενών χαρακτήρων και αλλαγών γραμμής από το τέλος της γραμμής
    line = line.strip()
    
    # Διαχωρισμός της γραμμής σε δύο ονόματα φίλων με βάση το κόμμα
    usernames = line.split(",")
    
    # Εκπομπή ενός ζεύγους <Όνομα Χρήστη, 1> για κάθε όνομα στη γραμμή
    for username in usernames:
        print(f"{username}\t1")  # Κλειδί: Όνομα, Τιμή: 1

