// Η κλάση DrawColorShape περιέχει μια στατική μέθοδο που συνδυάζει την ανάγνωση
// μιας τιμής εισόδου (πιθανώς για το χρώμα) και τη σχεδίαση ενός σχήματος.
public class DrawColorShape {
	
	// Η στατική μέθοδος drawColorShape() δέχεται ως παραμέτρους ένα αντικείμενο Shape
	// και ένα αντικείμενο Input. Αυτά μπορούν να είναι οποιασδήποτε υποκλάσης του Shape και του Input,
	// κάτι που επιτρέπει ευελιξία στον χειρισμό διαφορετικών σχημάτων και πηγών εισόδου.
	public static void drawColorShape(Shape aShape, Input input) {
		
		int color;
		
		// Χρησιμοποιούμε τη μέθοδο read() του αντικειμένου Input για να πάρουμε μια τιμή,
		// η οποία μπορεί να αντιπροσωπεύει το χρώμα του σχήματος.
		color = input.read();
		
		// Πολυμορφική κλήση: καλείται η μέθοδος draw() του αντικειμένου Shape.
		// Ανάλογα με την υποκλάση του Shape που δόθηκε (π.χ., Circle ή Square),
		// εκτελείται η κατάλληλη υλοποίηση της draw().
		aShape.draw();
	}
}

/*
Η κλάση DrawColorShape προσφέρει τη μέθοδο drawColorShape(), η οποία χρησιμοποιεί δύο αντικείμενα, 
ένα Shape και ένα Input. Η μέθοδος παίρνει μια τιμή μέσω της read() από το 
Input (πιθανώς για να αντιπροσωπεύσει το χρώμα του σχήματος) και, στη συνέχεια, 
καλεί την πολυμορφική μέθοδο draw() του Shape. Χάρη στην πολυμορφία, 
μπορούμε να δώσουμε οποιαδήποτε υποκλάση του Shape (όπως Circle ή Square) και η draw() 
θα καλεί την αντίστοιχη εξειδικευμένη υλοποίηση για το συγκεκριμένο σχήμα. 
Αυτό προσφέρει ευελιξία, καθώς η μέθοδος μπορεί να χειριστεί διαφορετικά σχήματα και διαφορετικές πηγές εισόδου.
*/