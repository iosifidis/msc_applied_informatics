
// Η κλάση Input είναι αφηρημένη (abstract), επομένως δεν μπορεί να δημιουργηθεί αντικείμενο
// απευθείας από αυτήν. Χρησιμοποιείται ως βάση για άλλες κλάσεις που θα επεκτείνουν
// τις λειτουργίες της.
public abstract class Input {
	
	// Η μέθοδος read() είναι αφηρημένη (abstract) και δεν έχει υλοποίηση εδώ.
	// Οι υποκλάσεις της Input θα πρέπει να την υλοποιήσουν με διαφορετικό τρόπο,
	// ανάλογα με τον τρόπο που διαβάζουν δεδομένα (π.χ., από πληκτρολόγιο, από αρχείο κλπ.).
	public abstract int read();

}

/*
Η κλάση Input είναι αφηρημένη και σχεδιάστηκε για να λειτουργεί ως βάση για κλάσεις που διαχειρίζονται 
εισαγωγή δεδομένων. Περιέχει την αφηρημένη μέθοδο read(), η οποία δεν έχει υλοποιηθεί εδώ. 
Οι κλάσεις που επεκτείνουν την Input θα πρέπει να δώσουν τη δική τους υλοποίηση για τη read(), 
με στόχο να εξειδικεύσουν τον τρόπο που διαβάζονται δεδομένα. Έτσι, μπορούμε να έχουμε διαφορετικές 
υλοποιήσεις ανάλογα με την πηγή εισόδου (π.χ., από το πληκτρολόγιο ή από ένα αρχείο).
*/