
// Η κλάση Shape είναι αφηρημένη (abstract), δηλαδή δεν μπορεί να δημιουργηθεί αντικείμενο
// άμεσα από αυτήν. Χρησιμοποιείται ως βάση για άλλες κλάσεις που θα επεκτείνουν τις
// δυνατότητές της.

public abstract class Shape {
	
	// Η μέθοδος draw() είναι αφηρημένη (abstract), οπότε δεν έχει υλοποίηση εδώ.
	// Οι υποκλάσεις της Shape θα πρέπει να την υλοποιήσουν με διαφορετικό τρόπο,
	// ανάλογα με τον τύπο σχήματος που εκπροσωπούν (π.χ., Κύκλος, Τετράγωνο κλπ.).
	public abstract void draw();

}

/*
Η κλάση Shape είναι σχεδιασμένη ως αφηρημένη για να λειτουργεί ως πρότυπο (base class) για άλλα είδη σχημάτων. 
Δημιουργώντας μια αφηρημένη μέθοδο draw(), επιτρέπουμε στις υποκλάσεις να καθορίσουν την συγκεκριμένη συμπεριφορά 
τους όταν σχεδιάζονται στην οθόνη ή σε κάποιον καμβά. Έτσι, κάθε υποκλάση, όπως η Circle ή η Square, 
θα έχει τη δική της υλοποίηση της draw() για να ανταποκρίνεται στις ανάγκες της.
*/